public class p_2_data_type_incAndDec{
    public static void main(String args[])
    {
        System.out.println("hello");
        // increment and decrement opearator

        int i = 50;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
        System.out.println(i--);
        System.out.println(i);
        System.out.println(--i);
        System.out.println(i);
    }
}
